package com.ideal.zsyy.entity;

public class RUserStatusInfo {

	private String userid;
	private String username;
	private String orgid;
	private String orgname;
	private double lon;
	private double lat;
	private String curdatetime;
	private String communitetime;
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getOrgid() {
		return orgid;
	}
	public void setOrgid(String orgid) {
		this.orgid = orgid;
	}
	public String getOrgname() {
		return orgname;
	}
	public void setOrgname(String orgname) {
		this.orgname = orgname;
	}
	public double getLon() {
		return lon;
	}
	public void setLon(double lon) {
		this.lon = lon;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public String getCurdatetime() {
		return curdatetime;
	}
	public void setCurdatetime(String curdatetime) {
		this.curdatetime = curdatetime;
	}
	public String getCommunitetime() {
		return communitetime;
	}
	public void setCommunitetime(String communitetime) {
		this.communitetime = communitetime;
	}
	
}
